package br.com.db1.exercicio02;

public class Roupa {

	private String marca;
	private Integer anoFabracacao;
	private Tamanho tamanho;
	
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public Tamanho getTamanho() {
		return tamanho;
	}
	public void setTamanho(Tamanho tamanho) {
		this.tamanho = tamanho;
	}
	public Integer getAnoFabracacao() {
		return anoFabracacao;
	}
	public void setAnoFabracacao(Integer anoFabracacao) {
		this.anoFabracacao = anoFabracacao;
	}
	
	
	
}
